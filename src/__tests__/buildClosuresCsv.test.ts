describe("buildClosuresCsv — output format details", () => {
  it("serializes ignoreTraffic=false as 'No'", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [1])];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment = {
      1: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };
    const fields: FinalFields = { ...DEFAULT_FIELDS, ignoreTraffic: false };

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, fields);
    expect(csv).toContain(",No,");
  });

  it("formats lon/lat with 5 decimal places", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [1])];
    const geos: RowGeo[] = [makeGeo(7.1, 46.2, 14)];
    const closuresBySegment = {
      1: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };

    const csv = buildClosuresCsv(
      rows,
      geos,
      closuresBySegment,
      DEFAULT_FIELDS,
    );
    expect(csv).toContain("lon=7.10000&lat=46.20000");
  });

  it("emits 'add' as the first column of every data row", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [1])];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment = {
      1: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, DEFAULT_FIELDS);
    const dataLine = parseLines(csv)[0];
    expect(dataLine.startsWith("add,")).toBe(true);
  });

  it("ends with a trailing newline", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [1])];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment = {
      1: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, DEFAULT_FIELDS);
    expect(csv.endsWith("\n")).toBe(true);
  });
});
