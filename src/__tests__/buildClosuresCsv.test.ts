// 4. Three overlapping ranges of the same segment → one merged range
// ---------------------------------------------------------------------------

describe("buildClosuresCsv — three overlapping ranges collapse to one", () => {
  it("merges three ranges sharing the same segment into a single range", () => {
    const rows: CsvRow[] = [
      makeRow("2026-04-29", "10:00", "11:00", [500]),
      makeRow("2026-04-29", "10:30", "11:30", [500]),
      makeRow("2026-04-29", "11:00", "12:00", [500]),
    ];
    const geos: RowGeo[] = [
      makeGeo(7.0, 46.0),
      makeGeo(7.1, 46.1),
      makeGeo(7.2, 46.2),
    ];
    const closuresBySegment: Record<number, ClosureRange[]> = {
      500: [
        makeRange("2026-04-29T10:00", "2026-04-29T11:00", 0),
        makeRange("2026-04-29T10:30", "2026-04-29T11:30", 1),
        // 11:00–12:00 starts exactly at the end of 10:00–11:00, but
        // 10:30–11:30 overlaps with it, so all three form one group.
        makeRange("2026-04-29T11:00", "2026-04-29T12:00", 2),
      ],
    };

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, DEFAULT_FIELDS);
    const lines = parseLines(csv);

    // All three original rows are emptied (only segment 500, which is stripped).
    // One merged row with start=10:00, end=12:00.
    expect(lines).toHaveLength(1);
    expect(lines[0]).toContain("2026-04-29 10:00");
    expect(lines[0]).toContain("2026-04-29 12:00");
    expect(lines[0]).toContain("500");
  });
});

