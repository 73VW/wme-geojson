// 5. Comma in reason → throws Error
// ---------------------------------------------------------------------------

describe("buildClosuresCsv — comma validation", () => {
  it("throws a descriptive Error when reason contains a comma", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [101])];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment = {
      101: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };
    const badFields: FinalFields = {
      reason: "Tour de Romandie, 2026",
      ignoreTraffic: true,
      mteId: "",
      comment: "",
    };

    expect(() =>
      buildClosuresCsv(rows, geos, closuresBySegment, badFields),
    ).toThrow(/comma/i);
  });

  it("throws a descriptive Error when comment contains a comma", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", [101])];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment = {
      101: [makeRange("2026-04-29T13:00", "2026-04-29T13:50", 0)],
    };
    const badFields: FinalFields = {
      reason: "Tour de Romandie 2026",
      ignoreTraffic: false,
      mteId: "",
      comment: "Auto, generated",
    };

    expect(() =>
      buildClosuresCsv(rows, geos, closuresBySegment, badFields),
    ).toThrow(/comma/i);
  });
});

// ---------------------------------------------------------------------------
// 6. Empty closuresBySegment → only header line
// ---------------------------------------------------------------------------

describe("buildClosuresCsv — empty closuresBySegment", () => {
  it("returns only the header when there are no closures", () => {
    const rows: CsvRow[] = [makeRow("2026-04-29", "13:00", "13:50", null)];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0)];
    const closuresBySegment: Record<number, ClosureRange[]> = {};

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, DEFAULT_FIELDS);
    const lines = csv.split("\n").filter((l) => l.length > 0);

    expect(lines).toHaveLength(1);
    expect(lines[0]).toBe(HEADER);
  });

  it("skips rows with null segments even if closuresBySegment is non-empty", () => {
    const rows: CsvRow[] = [
      makeRow("2026-04-29", "13:00", "13:50", null), // not validated
      makeRow("2026-04-29", "13:10", "14:00", [101]),
    ];
    const geos: RowGeo[] = [makeGeo(7.0, 46.0), makeGeo(7.1, 46.1)];
    const closuresBySegment = {
      101: [makeRange("2026-04-29T13:10", "2026-04-29T14:00", 1)],
    };

    const csv = buildClosuresCsv(rows, geos, closuresBySegment, DEFAULT_FIELDS);
    const lines = parseLines(csv);
    expect(lines).toHaveLength(1);
    expect(lines[0]).toContain("101");
  });
});

// ---------------------------------------------------------------------------
// 7. Output format spot-checks
